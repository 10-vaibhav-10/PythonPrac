#grade based on student marks

marks=int(input("Enter your marks"))

if(marks>=90):
    print("A Grade")

elif(marks>=80 and marks<90):
    print("B Grade")

elif(marks>=70 and marks<80):
    print("C Grade")


else:
    print("Grade D")

